            <div id="subtitle" class="croisant"><h3>Sitio Oficial</h3></div>
            <p> created by Cristian Avilés & Samuel Cabezas 2016
            <p>Banda de Rockpop Alternativo. Cuenta con 5 integrantes, se forma en Santiago de Chile a mediados del año 2013 como "Numbed" nombre con el cual tuvieron lugar en algunas presentaciones en la comuna de San Bernardo.</p>
            <p>Comienzan a grabar su primer EP el mismo año junto a Marcelo Donoso, y en el transcurso cambian su nombre para definirse como "Rieles". Con este, lanzarán lo que llamaron "Sonidos al azar" EP, cuenta con 5 temas, de temática introspectiva.</p>
            <p>Los miembros de la banda son Julissa Olivares en las voces, Ivan Avilés y Juan Pablo Lara en guitarras, Tomas Nahum como bajista y Joel Donoso en la batería.</p>
